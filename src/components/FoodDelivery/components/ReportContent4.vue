      <h1>外送員最害怕的事：車禍</h1>
      <p>國慶連假，臺灣連續發生了 2 起外送員死亡車禍。一位是才上線 2 天的空腹熊貓司機，在送餐途中被小貨車撞擊死亡；一位是 Uber Eats 的外送員，送餐途中與轎車擦撞，送醫後不治。</p>
