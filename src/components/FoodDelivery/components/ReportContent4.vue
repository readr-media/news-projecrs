      <h2>勞動部勞檢揪出假承攬真僱傭</h2>
      <p>勞動部因應死亡車禍緊急啟動了七大食物外送平台的勞檢，僅有「戶戶送」被認定為是承攬關係，其餘的業者都因為有指揮監督外送員的行為<PopUpNotation :text="notations[0]" />，被認定為是僱傭關係，遭到勞動部開罰。</p>
