      <p>立委林靜儀認為，認定僱傭關係固然重要，但這只是告訴平台業者該怎麼樣在形式上去除「從屬性」而被認定為承攬制，「國家無法單靠勞動法來進行市場管制，主管機關應該採用不同的工具來管理。」</p>
      <p>林靜儀主張勞工保險是最低的保障，可以透過訂定事業管理規則來規範從事外送員工作之前需要先有保險（可以由雇主提供，或由外送員自己加入工會加保），並加強「承攬」關係的勞動者權益。</p>
      <p>而台北市勞工局參考德國在「勞工」和「自營作業者」兩種勞工身份之間的「類勞工」身份，讓平台跟外送員保持承攬關係，仍部分適用勞基法，在 11 月 1 日推出「外送平台業者管理自治條例」草案，目前已通過市政會議，待市議會審查。</p>
      <p>草案內文明定平台業者應為外送員投保一定額度之保險、於外送員發生職災事故 8 小時內通報義務、需按季公布交通事故資料等等。台北市勞動局選擇直接跳過身份爭議，以自治條例落實管理，強制外送平台業者為外送員提供保險。</p>
