      <p>綜整訪談結果，大多數的外送員對於公司是否提供保障並不是太在意，他們擔心的還是適用《勞基法》後，工作的彈性會受到限制。</p>
      <p>但保障與收入真的難以兩全嗎？戶戶送可能是一個解答。戶戶送的外送員蘇柏豪跑外送 3 年來，出了 4 次車禍，他完全體認到這份工作的風險，卻還是不希望直接適用《勞基法》影響到彈性工時，也認為勞保職災給付認定困難，賠償又不優渥，「我覺得公司可以幫忙負擔意外險，讓這份工作維持承攬的彈性工時，又有保障，就是理想的答案。」</p>
      <p>戶戶送為所有的外送員在上線的前一小時、下線的後一小時提供意外險保障，它給外送員的報酬也是目前業界最高，平均一單 90 至 100 元，按照里程計費。戶戶送臺灣區總經理羅家聰表示，戶戶送外送員的平均時薪可以達到 200 至 300 元 。</p>
      <p>只是這樣的公司擴張畢竟保守，蘇柏豪在戶戶送剛進到臺灣的時候就去申請，排隊排了 7 個月才輪到他去面試。目前戶戶送的外送員人數約有 1000 人，僅佔全台外送員數量的 2.2%。</p>
