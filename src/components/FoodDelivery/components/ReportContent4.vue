      <h2>業者以自律規範因應</h2>
      <p>10 月 24 日，臺灣協作暨共享經濟協會以及多家美食外送平台業者共同發表了「美食外送平台業者自律公約」，要以自律的方式回應這個議題。公約內容有確保外送員安全權益、食品安全、交通安全等等，響應的平台業者包含 Uber Eats、戶戶送、GoGoVan、Lalamove 四家，但目前還未看到營運模式調整。</p>
