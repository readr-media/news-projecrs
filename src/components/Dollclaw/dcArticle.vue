  <p>「夾娃娃機的商品，很難都是正版的。尤其是社區型的，商品都是盜版的居多，因為要養客人，設定好夾一點，希望你明天還會再來，量大，就會選擇便宜的盜版商品進貨。」Andy 解釋，「而像海螺蠻腰這種藍牙喇叭要經過 NCC 無線射頻的認證，雖然台灣有代理商，但後來量大，大家都從大陸進來。」</p>

  <p>「而且，你買到有雷射商標的也不一定都是真的啊！雷射貼紙一張才10元，雖然娃娃臉有點歪，但它有貼，你也不知道是正版盜版。所以台主就很怕消費者打電話叫警察。」Andy 提到。</p>

  <p>阿勳也說，雖然現在沒有任何案例因為經營夾娃娃機業被起訴，但機台被警察扣住，就算後來還我，先不說這一個禮拜的業績，客人也會覺得很奇怪，你是不是做了什麼奇怪的事？現在這麼競爭，就很不利，「所以台主都會盡量跟客人息事寧人，真的有人要盧就送他啦，因為不管怎麼樣你都是賺錢。」</p>

  <p>商品的正版盜版還是經營者自己的選擇，但關於機台的合法與否，才是許多新進到這個產業的人覺得不公平的地方。</p>

  <p>「大家應該很直覺會認為夾娃娃機是電子遊戲機吧？電子遊戲機是遊藝機台，要擺遊藝機台需要遊藝場執照，一張平均轉手價格是四百萬，根本不會有人為了開夾娃娃機店去搞一張這個。」新進的機台商 Billy （化名）提到。</p>

  <p>「所以，現在四處可見的夾娃娃機，叫做『選物販賣機』，我覺得光這就是很荒謬的事。」Ａ苦笑，「選物販賣機應該是自動販賣機那種，你投 10 元，可以拿到 10 元的飲料。但是夾娃娃機是你可能投 10 元卻得到 100 元的東西，這叫『涉悻性』，是賭博機台最重要的標準。」</p>

  <p>但讓 Billy 覺得不公平的還不是定義問題，而是明明同樣玩法的機台，送到經濟部審議時，有時候是被評定為「益智類電子遊戲機」、有時候卻被評定為「非屬電子遊戲機」，也就是不需要遊藝執照就可以擺的。</p>

  <p>行政院消保處在聯合稽查後，就曾經公開表示，即使經濟部在2000年後召開多次會議，後來透過函釋定義夾娃娃機只要符合「設定保證取物價格」、「物品價值與售價相當」、「不可影響取物可能性」、「不更動操作流程或影響操作結果」四大原則，就是「選物販賣機」，屬於「非屬電子遊戲機」認證。</p>

  <p>但業者以此原則製造這類的娃娃機送審，評鑑委員會卻評鑑為「益智類之電子遊戲機」，前後評鑑標準顯然不一致。</p>

  <p>為了驗證，我讀完了經濟部 2000 年至 2018 年共 247 份審議紀錄。手動把送審的夾娃娃機依照遊戲流程分成四類，發現就算玩法一樣，有的被評為非屬電子遊戲機，有的卻被評定為益智類。還出現了同樣內容的機種，再兩次審議（間隔一個月）結果卻完全不同的狀況。</p>

  <p>Billy也提到，很多人為了通過審查，會故意將玩法寫成「投到商品價格遊戲才開始」，也就是說，假設娃娃機內的商品為 100 元，玩家就要投到 100 元才能開始夾，但遊戲只有一局，就是無限次夾到為止，「但審查過了，實際擺在店面裡讓大家玩的玩法就根本不可能是這樣。」</p>

