  <p>「這真的是天時地利人和。」資深玩家 Andy下了一個簡單的註解，「<strong>一切都是從分租模式開始。</strong>」</p>

  <p>「夾娃娃機以前是一個很封閉的市場，並非一般人可以參與的。它的營收很高，但是成本也不便宜，機台一台要兩萬，一間店三十台，就要一百多萬。雖然看起來不多，但說少也不少，快速展店是不可能的。」 Andy 表示。</p>

  <p>「但某一天，有人發明了把機台分租出去的模式。」Andy解釋，租下店面跟機台的「場主」把夾娃娃機分別租給不同的「台主」，對台主來說，一個月 5000 、 6000 元的租金並不是很高的門檻。而場主的成本馬上就回來了，就能馬上拿著回收的一百萬去開下一間，形成快速複製的關鍵。</p>

  <p>只不過，這個「人」是誰，江湖上有很多不同的答案。有人說是北台灣最大的機台銷售商、也有人說來自台中的逢甲夜市，來源似乎已經不可考了。</p>

  <p>就像每個傳奇故事的開頭，因為「某人」的起心動念，改變了整個產業的遊戲規則，讓夾娃娃機不再是有資本的人能夠寡占的事業。</p>

  <p>「我平時一半時間在台中，一半時間在板橋。某次在逢甲夜市聽到朋友的工讀生在做生意，我很驚訝，想說一個小小的大學工讀生可以做什麼生意？才得知這個娃娃機分租的模式。」本身也在做生意的 Andy 對可以賺錢的事情都很好奇，他抱著好奇回到板橋觀察，發現店也是開得如火如荼，就開始研究。</p>

  <p>「這個產業一開始真的很封閉，有在做的都是擺夜市的流動攤販，都不太願意教別人怎麼做。」Andy 苦笑，「我就是半夜趁他們補貨的時候，請他們抽菸吃檳榔，拜託他們教我。學完之後才自己下去做。」</p>

  <p>Andy 進場的時間是 2016 年底，正好是夾娃娃機店面數量開始暴漲的時候。夾娃娃機的瘋狂展店正開始侵蝕我們熟悉的街景。</p>

