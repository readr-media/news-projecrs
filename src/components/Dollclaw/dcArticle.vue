  <div class="descbox">
    <p>夾娃娃機到底取代了哪些店？我們與 g0v 參與者 RonnyWang 合作，取得自 2016 年 4 月開始的財政部稅籍登記變動資料約 210 筆，嘗試拼湊出這個問題的面貌。（資料限制：由於是「稅籍登記」，若規模較小的店可能就不會有。據受訪者指出，有些連鎖夾娃娃機店也只會登記在一家公司名下）</p>
  </div>

  <p>對於這些電子機台快速竄出的原因，有人預測是無人店的商機，有人認為是閒置店面的救星，有人則認為是百貨零售業的衰退。立委管碧玲也曾在質詢時提到，在房租漲價的壓力下，許多特色店家被迫消失。而新北市也針對市內夾娃娃機清查，發現夾娃娃機高度集中在學校附近，還嘗試用自治條例來管理。</p>

  <p>「我倒覺得有點倒果為因了。」 Andy 當然不否認這些趨勢的推測，但瘋狂展店的原因的最大功臣就是分租模式，「是因為店主很快就能拿回投入的資金，所以市面上只要有空的店面就進去了。而且做夾娃娃機業很簡單，不需要太多裝潢，就算後來發現店面生意不好，那也沒差，賠個押金就能搬到下一間去。如果資金模式沒有改變，有再多空的店面也不會有人去租。」</p>

  <p>「目前來講，已經開到沒什麼地方可以再開了。」Andy 說。</p>

  <p>而與天天合夥的阿勳印證了 Andy 的說法，「去年九月到今年過年時，是最瘋狂的時候。那時候我們正在找新店面，上 591 租屋網看，一間一間打去問，只有兩種回答。一種是很幸運還沒被問過的新物件，一種就是它不租給夾娃娃機，早就被問到煩了。」</p>

  <p>「我想要租一台機台，還要排隊，去年初的時候，我被候補到第 30 個⋯⋯。」從玩家變成台主的瑄瑄苦笑。</p>

  <p>「想投入的原因，就是上班族的薪水真的太低了吧！工作壓力又大，下班就想找一點事情放鬆。」瑄瑄說，「我覺得投幣下去的哐啷聲很舒壓。我也不是喜歡裡面東西，只是想要把它夾出來，很有成就感，練一練就能變得很厲害的感覺。」</p>

  <p>「一個月就算只賺兩、三千元，就等於是老闆加薪耶。進入的門檻又不高。」瑄瑄提到。</p>

  <p>分租模式讓寡佔的經濟分散到這些用小錢創業的苦悶上班族身上，而它還不是一個容易被「統一」的江山，因為要能經營得久遠，得靠創意。</p>

  <p>「我們一開始很排斥分租，想說好賺啊，幹嘛不自己賺，後來才發現不行。場主就算自己下去直營，也很難做超過四到五台，因為你的創意有限，精力也有限。」阿勳提到，台主要補貨，又要讓自己的機台夠有創意才能吸引人來玩，「真的是租給別人經營比較划算。」</p>

  <p>小芊就是這時候進場的。一天深夜，我跟著小芊來到她家附近的夾娃娃機店，她掏出一大把鑰匙熟練地打開機台的玻璃門，「有時候都要找不到自己家的鑰匙了！」她大笑。</p>

  <p>從事服飾業的她，在網路上看到夾娃娃機的影片覺得有趣，玩上了癮，為了讓自己不要花太多錢，就租了三台機台，一台在上班地點附近，兩台在家附近。</p>

  <p>平常，她就帶著一個裝著娃娃的大袋子搭公車上班。上班前巡台補貨，下班回家時也巡台補貨，最好的時候，一個月可以有四萬元的進帳；差的時候也有一、兩萬。</p>

  <p>跟著她巡完家附近的兩台機器，小芊還展示了一下夾娃娃的技巧給我看，瞄了一下手錶，不過才 15 分鐘。她跟我告別，說要回店裡再玩一下。我問她，是會跟客人交流的那種台主嗎？她說不會，「不然被客人坳或是嫌為什麼那麼難夾，很討厭啊！」</p>

  <p>不同於一般的服務業需要時時在店裡面對客人，無人商店給了小資族一個能夠用零碎時間賺取外快的機會。但這一行也不是那麼好做的。</p>

  <p>「一家店要做得好，真的是大家（台主們）要團結才比較有可能，但這就很難。」天天提到，他現在每一家店都會自己留個幾台，把難度降低，才不會讓玩家覺得這家店的機台都很難玩，就不來了。</p>

  <p>Andy 也提到，由於同一家店有很多不同的台主，彼此都是競爭關係，難免會相互模仿，也常常吵架，「像我們常常在洞口擺好夾的娃娃吸引客人來，那隔壁台的台主看到你擺好了，就跑去把它全部夾走。你會不會生氣？如果場主又沒有出來維護秩序，就會出事，最激烈就是跑去跟警察檢舉。」</p>

  <p>更有甚者，競爭還激烈到連同業都在檢舉。阿勳就提到，自己的店被檢舉不符合土地使用分區，說是工業用地不能開店，「這連房東都不知道耶，在我租之前，這裡開三媽臭臭鍋，我店的對面是清心跟麥當勞，結果我不能在這裡開夾娃娃機店？」</p>

  <p>不過，如果沒什麼問題，為什麼要害怕被檢舉？</p>

  <p>「這個產業政府如果要嚴抓，應該很快就熄掉了，因為有太多不合法的空間。」阿勳說，「正確來說，是我連自己是不是在做合法的事情都不知道。」</p>

