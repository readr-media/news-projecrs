  <div class="descbox">
    <p>若依照字面解釋「選物販賣機」，則「投入商品對等價格才能開始遊戲」的玩法最符合，近期也看到非常多夾娃娃機想用此方式通過審查。但就連這類的機器，也有 11 台被評定為電子遊戲機。</p>
  </div>

  <p>而設有「保夾金額」（未夾中就要重新投幣，但投到商品金額時則不限次數到夾中為止）的機型，在 2002 年首度被評定為「非屬電子遊戲機後」，後來再以同樣玩法送審的機台，卻常有不同的命運。</p>

  <p>歷年來，也有用「投10元獲得（如彈力球、鉛筆等）商品，夾娃娃為附屬遊戲」這種機型，先用低價商品創造對價關係，讓夾娃娃成為附屬遊戲。這類機器原本被評定為非屬電子遊戲機，但之後就算遊戲說明書內容長得一樣，還是被評定為益智類。</p>


  <p>經濟部並未針對我詢問的評鑑結果做太多解釋，只強調在 6 月 13 日後已經協調出十大規則，並寄給相關產業人士。而之所以會有不同的評定結果，「通常是沒有標註清楚」，經濟部並不承認內容完全相同。</p>

  <p>而Ａ直接告訴我，業界都知道，能否通過機台認定的潛規則，就是加入（自動販賣商業同業）公會，「我並沒有介意加不加入公會，只是覺得評鑑方法太奇怪。我們就是按照公會通過審議的選物販賣機二代機台的設計標準去做機台，但送審就是不會過。這樣根本不合理。」而公會理事長以公事繁忙婉拒了採訪。</p>

  <p>「但是呢，這個產業還是一如往常的瘋狂。」Ａ苦笑，「明明知道審核通過的機率很小，而且一但沒過（被列為益智類），就等同於被列入一定要遊藝執照才能擺的黑名單，還是有很多勇者把自己的機台送去經濟部審核。」</p>

  <p>儘管規則模糊，還是有人願意在這個充滿潛規則的市場嘗試畫出自己的地盤。天天跟阿勳選擇了機台業當作進入點。</p>

  <p>「開了店，我就想什麼東西都自己來比較方便。例如兌幣機會被蓄意破壞啊，光換一個元件就要九千元；娃娃機的玻璃門被砸，兩千元就飛了。這都是無人店的風險。但自己做就差很多。」有電工背景的天天直接把夾娃娃機搬到家裡開始研究，跟阿勳合作，從中國進口零組件，生產自己的機台。</p>

  <p>必須要從中國進口，是因為台灣的製造商不太願意把關鍵的零件放出來。你要買機器，就得整台買走。阿勳選擇直接去中國珠三角工業區的去生產聚落跑一趟，談好零組件生產的訂單，帶回台灣組裝。</p>

  <p>阿勳也提到，台灣以前是做賭博機台起家的，但老一輩的都跑去中國了，「他們都說夾娃娃機以前是最不賺錢的，沒想到現在異軍突起。光是其中一個產業聚落，最熱的時候一個月出貨就出到一千多台。」</p>

  <p>「不過現在大概只剩兩、三百台了。」阿勳說，從數字就能看出衰退的跡象，但他們並不擔心，自己做的好處，就不用跟風，在產業衰退之時還能運用箇中原理創造出新的商機。</p>

  <p>「但現在不知道政府會怎麼動，是最大的風險。」阿勳期盼政府能有明確的管理規則，但也是既期待又怕受傷害，畢竟管得太嚴，就「不好玩了」。從近期的結果看來，政府對於管理和取締有愈來愈多的共識形成，潛規則能不能被打破、確立下正式的規則、最後又是誰能夠留在遊戲場上，大家都還想要賭一把。</p>

  <p>Andy 手上也已經沒有店了。他提到以前一台夾娃娃機一個月可以賺到一萬元以上，現在只剩五千，還有一半沒賺錢，「要見好就收啊！」</p>

  <p>「我覺得最後賺到的只有兩種人，一個是賣貨的業者，一個是賣機台的人。其他的人，大概就是不同程度的小確幸吧，賺到小錢，跟小小的成就感。」Andy 下了這樣的結論。</p>

  <p>想起跟小喵一起去夾娃娃的那天，我坐在她的機車後座，等紅燈時，看到一群人停在路邊盯著手機看，看起來是在抓「寶可夢」，小喵還猶豫了一下要不要加入，後來還是繼續我們的採訪行程。</p>

  <p>我看著在夜色裡被手機螢幕照亮的一張張臉，跟站在夾娃娃機台玻璃前盯著爪子看的臉孔其實很類似。旁人看來空虛，他們卻擁有現實以外的另一個世界。</p>




    
    
