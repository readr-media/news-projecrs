        <span>如果觀察總體的補助款變化，2016 年其他臺商所獲取的補助款，跟旺旺一樣有所提升。對此，《日經新聞》表示這是因為臺灣再次政黨輪替，由民進黨的蔡英文獲選為臺灣總統，北京當局不停加大對台企的優惠。顯示包括外媒在內，大多數人對於中國補助款的第一個反應仍是政治因素。</span>
      </p>
      <p>
        <span>但如果從中國政策的面向探討，2013 年習近平上任以來，相繼頒佈「一帶一路」、「中國製造 2025」、「十三五規劃」等政策，確實對於部分產業，包含製造業釋出相當程度的利多。</span>
      </p>
      <p>
        <span>以鴻海集團的富士康工業富聯來說，2018 年 6 月上市後，首次公開募集 271 億人民幣——此為中國 A 股自 2015 年以來最大的 IPO。工業富聯在招股說明書中強調，會積極響應「中國製造 2025」的規劃，聚焦電子智能製造的前沿技術，致力於新產品的研發創新與生產實踐。</span>
      </p>
      <p>
        <span>事實上，中國各地方政府在中央頒布《中國製造2025》之後，就已開始在地方開設許多相關的專項資金，支持相關產業的升級。像是，</span>
        <a
