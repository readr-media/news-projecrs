      </p>
      <h2>
        <span>學者：當你拿中國的錢，就代表可能被收買</span>
      </h2>
      <p>
        <span>一直以來，臺灣社會針對兩岸的經貿問題，有兩種不同的立場，一種是認為政府應該「在商言商」，讓經濟回歸市場機制；另一種是認為中國政府會通過經濟手段，在台培養利益的代言人。因此，臺灣政府應該以政策清楚規範臺商。</span>
      </p>
      <p>
        <span>清華大學社會所副教授沈秀華的想法傾向於後者，她強調臺資在兩岸之間，從來就不只是經濟意涵，「在改革開放之後，臺灣人到中國投資，就已經說是『以經促統』的手段。早期臺資對中國的經濟有重要地位，後來（對比其餘外資）就沒那麼重要了。但最近因為中美貿易戰，臺資的政治意涵變得愈來愈強。」</span>
      </p>
      <p>
        <span>她表示，必須關注旺旺和富士康在中國的補助款的原因，「如果知道中國的錢有統戰意味，那你可以接受被統戰。不過今天郭台銘要選舉，旺旺旗下有媒體。當你拿中國的錢，就代表可能被收買，回來做臺灣媒體或選舉，我該怎麼信任？」</span>
      </p>
      <p>
        <span>蔡衍明對於中國的態度，向來表現得相當明白。2008 年 3 月，當旺旺在香港上市，</span>
