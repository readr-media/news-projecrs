      </p>
      <p>
        <span>在學測成績開放之前，臺灣學生大多是通過另外四種方式進入中國的大學：港澳台聯招、直接考中國高考、個別學校單獨招生，或是以大學學歷申請試讀生。其中港澳台聯招與個別學校單獨招生是比較多人使用的管道，但這些申請者更多是當地臺商子女。</span>
      </p>
      <p>
        <span>連憲成的父母是在北京的中小企業臺商，也因此，他相對同齡的同學，對中國的社會環境更為熟悉。他說自己決定到中國唸書，很大一部分就是因為可以用學測成績申請大學，方便很多。他補充，自從高一從媒體得知這個資訊之後，就確定之後要通過這個管道申請中國的大學。</span>
      </p>
      <p>
        <span>同時他也觀察到，臺灣學弟妹逐年增加，因此成立「上海交大台生聯誼會」，「這就類似於臺灣的大學裡雄友會這種，定期聚餐、辦活動。如果學校希望在臺灣招生，也會到臺灣辦分享會。」</span>
      </p>
      <p>
        <span>就讀上海交大期間，連憲成會在寒暑假期間，回到雄中、雄女辦分享會。他表示臺生們大部分還是關心比較實際的問題，像是怎麼申請，或是課業是否跟得上，政治問題反倒不多。</span>
      </p>
      <p>
        <span>2014 年 3 月 18 日，臺灣社會爆發的太陽花運動，參與運動的成員不少是年輕人。連憲成的雄中同學們也有參與，但他當時人在上海，是在網路上密切關注這個運動，「當時我有跟我朋友說，愈是這樣，這裡對臺灣青年的政策會越優惠。到後面這件事也算是印證了。其實這也很合理，不會來大陸的就是不會來，那會想來的，就是多一個東西驅動他去做這件事。」</span>
      </p>
      <p>
        <span>長期觀察中國對台政策改變的沈秀華表示，太陽花學運使得中國政府開始意識到與臺灣年輕族群的距離，接著調整策略，希望吸引到這一群人。</span>
      </p>
      <p>
        <span>中國國務院就在 2014 年 9 月喊出「大眾創業、草根創業」、「萬眾創新、人人創新」等口號。過沒多久，各地方政府相繼成立兩岸創業基地，除了提供空間、住宿，也喊出會為臺灣的新創團隊，提供優惠措施。</span>
      </p>
      <p>
        <span>只不過，起初創業優惠政策喊得風風火火，過了幾年再回頭看，就會發現大部分「兩岸青創基地」運作的並不是太成功。現任「中華傑出青年交流促進會」（簡稱傑青會）的理事長陳長風，就曾經在 2015 年開始，協助運營南京青創基地。</span>
      </p>
      <p>
        <span>但他表示，這幾年官方已經很少談「創業」，主要是因為國台辦授牌的單位，做出成績的並不多，「很多地方做這種青創基地，只是跟著（中央）政策走，這種先天就沒有重視的基礎。實際上，對地方來說，吸引這些沒有基礎的新創企業，對當地的經濟（稅收）一點幫助也沒有，就是一個政策風向。更何況，這其實是個一門專業。如果當時各地的招商局有認真在做，還是有好的可能性。」陳長風提到目前運營的還不錯的創業基地，大概只剩東莞和福建。</span>
      </p>
      <p>
        <span>實際上，不少新創團隊，起初確實衝著優惠和市場前來瞭解，但最終留下的不多。根據中央社的</span>
        <a
          href="https://www.cna.com.tw/news/acn/201903270191.aspx"
          target="_blank"
          rel="noopener"
        >
          <span>報導</span>
        </a>
        <span>，當地官員就曾表示，「兩岸青年創業政策是有政治目的的，創業失敗沒關係，很多人沒來過大陸，至少來看看」。</span>
      </p>
      <p>
        <span>「大陸還是蠻重代表性的。早期來說，年輕人對他們來說，不見得是有影響力的群體，後來慢慢也開始需要重視。目前去那就業的年輕人越來越多，就包含台生到那唸書，就會在當地就業，畢竟他們的生活圈就在那裡，這跟早期臺商的脈絡不一樣，以前即便去了十年、二十年，還是會覺得自己去大陸出差。」陳長風說。</span>
      </p>
      <p>
        <span>「中國是想創造一個好的創業環境、一個經濟榮景的意象，這是中國政權持續的重要性。」鄭志鵬說，目前懷有「中國夢」的臺灣人，更多的是還沒有去過中國的人。</span>
      </p>
      <p>
        <span>儘管近幾年，中國官方的確通過調整對台政策與加大優惠希望吸引更多台青，但在一個資訊流通的年代，臺灣年輕人對於中國的自主性了解，也比過去多了不少。為了追求更好的發展機會，前往一個大的市場，對於許多年輕人來說，可能是再自然不過的邏輯。</span>
      </p>
      <p>
        <span>以連憲成來說，他從大學開始在中國起步，逐步建立起自己生活圈，未來的就業也以中國為主，他們著眼的，確實就是那些更為實際的因素，「我們也知道他們有（政治）意圖，但他們也確實放了實質的東西。我們作為學生，其實也不太在意意圖，反正就是求學找工作，基本上就是淡化一些政治問題。」</span>
      </p>
      <p>
        <span>中國把政治意圖包進優惠措施中，來招攬它想吸引的人，已經是慣用手段。只是隨著兩岸政經環境的變化，臺灣人對「中國因素」是更敏感還是更接受？中國的招攬方式還能不能如過去一樣成功？值得繼續觀察。</span>
      </p>
