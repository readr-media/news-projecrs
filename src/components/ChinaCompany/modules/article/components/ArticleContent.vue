      </p>
      <h2>
        <span>「自由港」香港，是否已經受到中資影響？</span>
      </h2>
      <p>
        <span>近期香港因《逃犯條例》修訂而引發「反送中」抗爭，如那句著名的口號「今日香港，明日臺灣」的意涵，臺港兩地的民眾在某種程度上，對涉及中國的議題有著特殊共鳴。但無論是歷史發展或是現階段政治現狀，兩邊還是有著差異。另外，光從外資政策來看，香港並不以屬地主義限制資金，但中國資金在臺灣的投資，是受到限制的。</span>
      </p>
      <p>
        <span>1993 年，香港股市的前 15 大市值公司，只有中信泰富一家是中國企業。如今，已經有 8 家都是中國企業。也就是說，在香港做生意，現在已經很難繞開中資企業。</span>
      </p>
      <p>
        <span>綜觀歷史，香港社會的強勢資金曾經過多次流轉，從英資、港資，到現在的中資，一方面這是香港作為亞洲金融中心所具備的開放性，另一部分也反映當下政治局勢。</span>
      </p>
      <p>
        <span>對於資本流動，莊太量傾向於站在「市場機制」的角度思考，而不是人為限制，「香港是自由港，不限制資金來源。你中資來了，可以給人民高工資，我們歡迎；你中資的電信公司來，可以給便宜的電話費，我們歡迎；你中國的銀行來，可以給便宜的貸款買房，我們歡迎。我們是市場主導，今天你資金從哪來的，根本不重要。香港沒有抗拒啊，給我好處就可以。」</span>
      </p>
      <p>
        <span>但中資投資港企，恐怕也不是全然沒有政治意涵。香港回歸以前，中資對於香港的投資是具備政治性的，協助主權移交的過程可以更為順利，當時更多的投資是通過中國國有企業。</span>
      </p>
      <p>
        <span>它甚至成為一種政治性展現。原先為港資企業的俊和發展集團，2014 年被中國新維投資買下，開始對佔中運動的立場趨於強烈反對。2014 年 10 月 9 日，俊和發展集團寫信給香港的四間大學，表示對於這些大學的學生領袖與教授參與佔中運動，深感失望，接下來將撤銷約 510 萬台幣的獎學金。</span>
      </p>
      <p>
        <span>此外，日前在《逃犯條例》修訂的爭議中，原先華人置業前主席劉鑾雄向高等法院申請司法覆核，希望了解條例的正當性。</span>
        <a
          href="https://www.thestandnews.com/politics/%E9%80%83%E7%8A%AF%E6%A2%9D%E4%BE%8B-%E5%8A%89%E9%91%BE%E9%9B%84%E6%92%A4%E5%8F%B8%E6%B3%95%E8%A6%86%E6%A0%B8%E7%94%B3%E8%AB%8B-%E5%BC%B7%E8%AA%BF-%E6%84%9B%E5%9C%8B%E6%84%9B%E6%B8%AF-%E6%9C%9B%E6%B8%9B%E7%A4%BE%E6%9C%83%E7%88%AD%E6%8B%97/"
          target="_blank"
          rel="noopener"
        >
          <span>最近他突然改變立場，表示撤回申請</span>
        </a>
        <span>，接著強調自己是「愛國愛港」的商人，以及「一貫全力支持香港特別行政區政府依法施政」。</span>
      </p>
      <p>
        <span>莊太量補充，不排除資本是有可能影響本地政治，「如果你是中資做事，在選舉的時候，他會指定你說投誰。但我還是覺得這是個人選擇，如果你不喜歡，那就別去中資公司做事。」</span>
      </p>
      <h2>
        <span>官方讓利的對象，逐漸從「臺商」擴大到「台青」</span>
      </h2>
      <p>
        <span>2018 年，國台辦頒佈「惠台 31 條政策」，內容主要分成兩個重點，一個是繼續針對臺資企業給予優惠與補助，另一個是廣大「臺灣同胞」到中國學習、創業與就業的相關優惠。前者與過往「招商引資」大同小異，後者則顯示出近年官方重視的新方向。</span>
      </p>
      <p>
        <span>「惠台政策一點都</span>
        <Hint
          :title="'不新'"
          :quote="'2005年4月，國民黨主席連戰赴中達成「兩岸和平發展共同願景」建立平台，由陳雲林宣布惠台 15 項措施。'"
        />
        <span>，它是一個整合型的政策，過去就有，現在只是拼出來。所以你說臺灣對於中國有沒有價值？一直都有。現在台青、台生、台師都是大量吸引的對象。中國從過去就一路吸引臺灣人，那他吸引怎樣的臺灣人，就代表他看重的是什麼。」鄭志鵬說。</span>
      </p>
      <p>
        <span>沈秀華補充，中國政府近幾年採取「人數」策略，吸引的對象已經從商界擴大到各年齡層。</span>
      </p>
