        <p>「有的學校會自己招募志工，彩虹媽媽、大愛媽媽都可以一起來（說故事），分享這個時間，由學校統籌。但是願意這樣做的學校真的很少，因為學校還要再花人力、時間去管理。」佑安認為這是類似彩虹媽媽這樣的組織能夠壯大的原因，憑著高度系統化，讓學校得以把志工「外包」給它們招募、培訓、管理。</p>
        <p>蔡惠婷提到，「學校老師畢竟有領薪水，可以對他們進行要求、也能規範。但對沒有領薪水的志工團體來說，卻沒辦法去進一步的規範、要求。」</p>
        <p>家長佑安也提到，一直以來，學校的志工系統都沒有組織化，「我並不是要怪學校，只是它已經成為一種約定俗成，沒有被任何法令規範說，校外組織入校需要什麼樣的公告、流程，都沒有。」</p>
        <p>且志工不僅在「晨光時間」入班入校，其他綜合課時間也能進入校園。從台中市議員黃守達提供的資料可以發現，雖然台中僅有 13% 的小學在「晨光時間」有宗教團體入校，但若計入其他課程時間，比例將提高至 35%。</p>
