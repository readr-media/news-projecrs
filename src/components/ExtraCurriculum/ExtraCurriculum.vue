        <p>「生命教育志工的勢力範圍與教育資源的分配有關。」性別平等教育大平台專案經理張明旭舉例，像是新竹縣的竹北、竹東地區，在中小學教師人力稀缺的情況下，多半學校都有生命教育志工團體入校。除此之外，各別家長能夠用多少心力關注、團體本身的資源、人脈，以及導師本身的意識，都會是能否對志工進行監督、以及決定生命教育志工影響力程度的因素。</p>
