        <h2>立意良善的「生命教育志工」如何成為校園的漏洞</h2>
        <p>1997 年，自殺連續首度進入國人十大死因的行列。因應自殺防治，台灣省教育廳（現已改制為教育部中部辦公室）開始推行生命教育。</p>
        <p>起初，由台中市曉明女中邀請一批學者，將天主教學校的倫理教育加以擴充，提出生命教育的基本理念。2001 年，時任教育部長曾志朗編列兩億元經費推動「教育部推動生命教育中程計畫」，以教育階段來區分，高中以上的生命教育是單獨設科，高職和國中小則將生命教育議題融入各科教學。將生命教育理念積極推動到全台各級學校。</p>
        <p>由於「生命教育」的內涵始終模糊，實務採「融入各科教學」實施，但因缺乏具體規劃，無適當的教材和師資，加上升學主義掛帥，課程地位始終薄弱，生命教育理念雖多獲得認同，但卻難以落實融入各科教學。</p>
        <p>在此同時，教育部的「推動生命教育中程計畫」裡曾列出提到希望能借助民間團體的資源，共同推動校園的生命教育，加上學者也呼籲應多和社區或相關民間基金會合作。民間各生命教育團體便開始陸續成立，包括使用靜思語進行教學的慈濟「大愛媽媽」、福智文教基金會的「讀經媽媽」等<EcAnnotation :content="'陳立言〈生命教育在台灣之發展概況〉，《哲學與文化》31卷9期（2004）、陳雅婷〈宗教性民間團體的國小生命教育課程在華人社會的擴散因素：以台灣彩虹愛家生命教育協會為例〉，《華人文化研究》5卷1期 （2017）'" />。</p>
        <p>1999 年，彩虹愛家生命教育協會的前身「Rainbow Kids 兒童教育中心」成立。除了積極培訓志工入班教學以及開發教材設計教案等，過去 10 年，彩虹志工更在全台各地及各級學校推廣、演出聖誕音樂劇。</p>
        <p>「志工是活化學校的方式，也能彌補學校教育不足的地方。」除了彩虹媽媽等說故事志工，她也以新竹不同地區發展出不同特色的志工團為例：如培養學校閱讀風氣的建功國小艷紫荊故事志工、常演出精彩戲劇的陽光國小凳子故事團，以及訓練孩子感覺統合的新創感統動感志工團。</p>
        <p>「其實彩虹愛家的聖誕劇真的滿好看的，以前當導師時，我都會偷偷期待，」待過國小 10 年經驗、現任新竹市東區市議員的蔡惠婷靦腆地笑了。</p>
        <p>除了彌補教學現場的不足，「對導師來說，彩虹媽媽提供的，就像是一種喘息的空間。」她從教學現場的情況解釋起：許多學校強制規定，導師需在週間某天晨光時間開會。那時，會需要人手遞補，協助教室內的看顧。</p>
        <p>「我們在面對的，其實不是 23 個孩子，而是 N 個家庭。」她比喻，導師的使命就像帶領孩子成為全人的人師。「所以就算不用開會，我們也把說故事志工進班的時間，當成『喘息服務』。在這段時間，導師可以改作業、改聯絡簿，我相信很多導師是樂見有人來幫忙分擔教務的。」</p>
        <h2>現行制度規管強度不高</h2>
        <p>學校仰賴志工提供服務，但對於志工卻缺乏系統的管理方式。現行制度下，國小晨光時間的志工團體活動管理僅有一紙「辦理要點」，來自教育部國教署 2015 年的「學校為豐富教學邀請校外人士進班協助教學」辦理要點，明列各校應自訂審核機制、原授課教師需隨班，且課程安排不得為特定政治團體或宗教信仰從事宣傳或活動。</p>
        <p>教育部在要點提醒下讓各校自行辦理，實際成果卻往往有限，從資料中就能發現執行成果有限的事實。首先是資料的掌握與實際狀況出現落差，如苗栗縣議員曾玟學就提到，就他的掌握，至少從去年九月到今年九月，彩虹愛家生命教育基金會確實有在苗栗校園內服務，但他向苗栗縣教育處索取的國小晨光時間校外團體調查資料中，苗栗縣的國小沒有一間學校回報有彩虹媽媽。可見校方根本沒有掌握入校的志工狀況。</p>
        <p>新北市議員戴瑋姍也曾在記者會上指出，根據彩虹愛家的資料顯示，雙北超過半數都有彩虹媽媽，但新北市政府提供的資料顯示，213 所公立國小，僅有 45 所有彩虹媽媽，遠低於實際數據，顯然主管機關無法掌握實際狀況。</p>
        <p>READr 執行的全台國小晨光時間調查的資料，也和彩虹愛家生命教育基金會在官網上宣稱的服務範圍有極大落差。</p>
