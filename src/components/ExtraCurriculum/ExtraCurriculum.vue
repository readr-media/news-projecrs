        <h2>變色的彩虹</h2>
        <p>今年 8 月 19 日，「吳少喬，我是女同志，也是媽媽」專頁張貼了<a href="https://www.facebook.com/story.php?story_fbid=425993754685005&id=293203214630727" target="_blank">〈被強塞的守貞教育〉</a>一文，指出有家長收到竹北興隆國小的傳單，二年級的學童全學年需上 12 堂彩虹媽媽的課程。文中更提及有家長抗議，過去有志工要求孩童簽署守貞卡，並以「嚼爛的口香糖」比喻發生過婚前性行為的女孩。</p>
        <p>接受採訪時，性別倡議者吳少喬在咖啡廳裡哭了，她沒有想到，當初揭露彩虹媽媽在晨光時間入校宣傳守貞教育和「傳教」的貼文，會如此改變她的生活。她拿出幾份資料，明列發文過後，數十個來自各處家長、學生、老師的自白與投訴案例，「這些都是真實存在過的案例，是彩虹愛家過往造成傷害的罪證，」她氣憤地說。</p>
        <p>例如，其中一份新竹縣某小學老師的投訴：「⋯⋯教授的團體稱為『彩虹媽媽』，學生是六年級的孩子，要上 12 到 15 個小時的課程。教材內容充滿各式各樣誘導性的問答，性別偏見，單方面的說詞和武斷的證據，並且要求孩子『立約拒絕婚前性行為』。」</p>
        <p>「六年級的孩子有些知道什麼是性行為，有些則否。不知道的孩子，就會在純粹被誘導的情況下，立下這個無理的約定。我不反對有些人支持婚前守貞，我甚至也不反對在教材裡出現支持婚前守貞的理由。我反對的是，這種單方面的說法在教育環境裡掌握了優勢的發言權，使得另一方的說法被排斥。⋯⋯」這位老師寫道。</p>
        <p>不只是彩虹愛家生命教育協會被抗議。台中市議員黃守達也提到，有家長提供課程錄音檔投訴，台中某間小學的校外志工在協助教學有許多不當發言，諸如「男生跟男生結婚是不對的，不用尊重」、「雖然有立法通過，但是不對」、「領養很可能造成亂倫」等等。這些不當發言發生在得勝者教育協會的「真愛守門員」課程，黃守達認為這樣的發言明顯違反《性別平等教育法》。</p>
        <p>三峽區北大國小自然科老師、科普作家陳民峰也<a href="https://opinion.udn.com/opinion/story/8806/1970114" target="_blank">舉例</a>：「我自己親眼見過兩個案例：一是我大學修習教育學程見習時，基督教的故事媽媽趁導師在教師會議時間，講了聖經的故事，講到了因為不信仰單一神的種種罪罰，回去以後學生就喊著要燒毀家裡神像。」</p>
        <p>「第二起案例則是在當實習老師時，看到其他班級發生的狀況，慈濟志工於學生早自習時，美其名教導生命教育，實則講到吃肉就很殘忍、吃肉會下地獄，學生回家便喊著只要吃素。這兩起事件家長都相當氣憤，學校也都立刻警覺到問題，並且對愛心媽媽做監督管理。」</p>
        <p>「家長最擔心的，是沒有人可以阻止這些事。」佑安氣憤地說，「學校說教材是團體的、團體推說這是個案⋯⋯但難道這些個案就沒有傷害嗎？這些個案數量很少嗎？」</p>
        <h2 id="chapter-3">彩虹媽媽的面貌</h2>
        <p>彩虹媽媽、竹北興隆國小前團長羅君儀受訪時提起近期的風波，講起來還是無奈：「我說我沒有宗教信仰，但沒有人想要來暸解。大家都選擇相信網路上的東西。」竹苗區辦公室主任彭淑貞接著說，「網路上的媒體，也都沒有打電話來問過我們協會。」</p>
        <p>羅君儀成為彩虹愛家的說故事志工是約 2 年前，因為自己的孩子上小學，「我們不是專業的（志工），剛好協會又有培訓、又有教材，把這個帶給小孩，只是希望大家更好，下一代更好。」</p>
        <p>「坦白說，當初會加入彩虹愛家，即使我沒有那麼認同團體理念。但我認真比較過，他們的東西是做得最完整的，我幾乎像是沒有選擇。」一位家長透露。</p>
        <p>這也是採訪過程中許多志工選擇成為彩虹媽媽的原因：受到密集的培訓課程、每週需集體備課一次的讀書會團體文化，且擁有改版多次、相對活潑的教材吸引。晨光時間的教學現場，志工多是根據事先審核過的教案，進行每週的課程安排。在現場，除了使用團體提供的教材，也會輔以繪本、志工本身主動蒐集的各式資源與孩童進行互動。</p>
        <p>其他團體志工使用的教材，如大愛媽媽使用的靜思語和《大愛引航》教材規模就相對薄弱，也不若彩虹愛家編撰的教材有針對一到六年級完整規劃。不同團體使用的典籍：如福智「讀經媽媽」使用的《弟子規》、《論語》、一貫道團體使用的《百孝經》等。或是在志工間、以及組織的緊密程度上，都不如彩虹愛家一般自成一套系統。</p>
        <p>笑聲宏亮的彩虹媽媽尚勤，在桌上攤開一本超過食指寬的活頁本，側欄寫著「彩媽筆記本」。裡頭每一個活頁，都塞滿了至少兩到三份的文件，是多年來彩虹愛家生命教育協會的內部培訓教材，上頭的筆記多半密密麻麻，內容包括備課、思考教學、體驗教育及班級經營、課室管理等教學知識與技巧；她還有另一本筆記本，就像是工作日誌，裡頭記滿了與其他彩虹媽媽媽共同備課的交流討論，及進班教學要注意的內容、步驟。</p>
        <p>在孩子升上小三後，尚勤恰好自職場高強度的工作退下，閒不下來的她，因為孩子某次回家表達了「喜歡學校裡的彩虹媽媽」才開始接觸，並受到彩虹愛家完整、有系統的組織型態吸引，思考後決定加入。</p>
        <p>彭淑貞是 18 年的「資深彩虹媽媽」。她原先就在教會兒童主日學講故事，「聽到故事志工培訓，想說，我已經很會講了，還要去培訓嗎？」彭淑貞笑說，後來才發現是「生命教育培訓」，她因此認識了彩虹愛家。</p>
        <p>「那時候聽彩虹秘書長陳進隆分享，如果你的生命剩下一年，你會要做什麼事情？那時候我身體不是非常好，我覺得生命只剩下一年，我想做一些有意義的事情。因為從小到大我真的不知道自己要幹嘛，我只知道天生我材必有用，但用在哪裡我不知道。」生命教育，從此成了彭淑貞的使命。</p>
        <p>「我小時候也一直聽到『天生我材必有用』這句話，一直不理解。」老家在中國江蘇的彩虹媽媽紫琳說：「我一直覺得自己是沒用的、我是多餘的。後面真的是接觸學校的生命教育我才真正理解。」</p>
        <p>紫琳回憶起第一次陪孩子去學校，校方徵求志工，「有清潔、導護，有人鼓勵我可以當故事媽媽，但我跟老師說，我還不會講故事，我去找個地方學一學再來吧？我不自信。但老師說不行，你就來吧！小孩子隨便你講什麼他們都喜歡的。」</p>
        <p>「我進去講了兩次，覺得心好虛。就覺得講不好啊，自己覺得講得坑坑疤疤的，不夠流暢，就覺得不行，應該要找個地方學，如何把故事講得更好。」諮詢別人以後，其他家長介紹她去彩虹愛家受訓，「一帶就是四年，內心我覺得沒有什麼奉獻精神，我只是為了自己孩子付出。」紫琳拿出滿滿一疊整班孩子親手做的卡片，笑了開來。</p>
        <p>這些生命教育志工，多半是大方的好人，她們面貌各異：有的是虔誠的基督教徒、有的是資深的佛教徒。有的志工什麼都不信，有的志工什麼都拜。當初她們加入各團體的契機和需求，不盡然相同；而唯一的共通點，是對於自己和團體行善的信念深信不疑。當一片善心被質疑，她們也非常挫折。</p>
        <p>紫琳強調，「其實我們在備課讀書會的時候，一再被提醒不要帶任何宗教進入教室。特別是我自己內心也排斥，政治跟宗教都不要進校園，要給孩子純淨的生命教育。」</p>
        <p>興隆國小後來因為壓力，停止彩虹媽媽入班，羅君儀提到，「志工媽媽們心理都受到很大影響。因為我們完全不是她講的那個樣子，興隆也完全沒有宗教色彩。」小巫也說，有班級導師跟他反應「很可惜」，還是會希望他們進班。</p>
        <h2>生命影響生命的爭議</h2>
        <p>「生命影響生命」，不僅是許多教育工作者謹記在心的理念，也是採訪過程中，接近 20 位來自各處生命教育志工異口同聲的信念；然而，究竟是什麼樣的生命，在影響怎樣的生命？</p>
        <p>「彩虹媽媽的組成其實常常是家庭主婦、自營業者，很少數從信望愛社團來的學生志工，通常也是以基督徒為主，」擁有長年經驗的輔導工作者日青（化名）提出她的歸納。</p>
        <p>日青觀察到，許多婦女在婚後成為全職家庭主婦，除了面臨傳統生育、教養的壓力，也要面對職涯上的停滯。此時，成為校園生命志工，除了可以為孩子所在的班級進行貢獻，經過接受系統性的培訓，也能帶來自我成長的成就感。</p>
        <p>「當妳問個問題，台下小朋友一呼百應時，就覺得擁有一個發話權。」她繼續解釋，除了自我增能的成就感，進入志工團體，許多成員相互支持、陪伴，團體便逐漸成為生命中的重心，「所以說當我們在批判彩虹媽媽的事情的時候，她們會那麼激動。因為對她們來說，那是她們現在生活最重要的一件事情。」日青說明。</p>
        <p>這些將生命投入的志工，也撐起部分的校園安全網。綜觀過去，曾有學校反映自從生命教育志工團體入校後，輔導室的業務減少；在高等及最高法院判決書系統中，也曾出現零星校園中妨害性自主的案件，是由生命教育志工首先發現、通報的<EcAnnotation :content="'如最高法院 108 年度台上字第 1504 號刑事判決，國小之「愛心媽媽」志工於晨間陪伴時，偶然發覺女童言行舉止有異，意外得知女童於4 歲時遭熟人輪流性侵；最高法院 97 年台上字第 6109 號刑事判決，志工媽媽請學童書寫家庭狀況，發現女童書寫：「家庭性侵害，我都不敢跟媽媽說」；臺灣高等法院 103 年侵上訴字第293 號刑事判決中，導師因上得勝者課程，意外透過牧師得知學生遭性侵；最高法院 105 年台上字第 3479 號妨害性自主的刑事判決中，發現者也是校園志工媽媽；最高法院 98 年台上字第 3782 號妨害性自主罪之刑事判決：「輔導老師[...] 提出之被害人書寫之得勝者作業簿內容、處理情形紀錄表」等。'" />。</p>
        <p>即便抱持著赤誠和善意，但「生命影響生命」。教育者本身的信念與生命歷程，會直接反映在與孩童的互動上。</p>
        <p>「我認為老師 100% 需要更多的努力，去提升自身的宗教、性平意識。」陳民峰提出他的見解：過去，志工在講述生命教育時，對於教材中的宗教相關字眼毫無自覺。而坐在教室後的導師，也對志工講述的內容缺乏相關的敏感度。教育者對自身行為的不自覺，也就導致孩童直接受到衝擊。</p>
        <p>陳民峰解釋，在尚未發展出釋讀與批判能力時，個別宗教的賞罰觀或排他性，抑或去脈絡化的教學，都對孩童的心智發展毫無助益。即便教師本身具備一定敏感度、能察覺到特定志工講的內容不適當，志工在教室進行教學的過程中，導師也很難直接喊停。</p>
        <p>「必須要正視宗教行銷存在教育界的事實！」陳民峰直言。他以不少早自習或者課後的讀經班為例，儘管現在各地方政府推廣的讀經教育已經去宗教化，但背後仍是由一貫道、福智、中台等宗教勢力推動。</p>
        <p>基隆市議員張之豪也提到，「原則上，我們其實只是要求學校回歸教育的中立。《憲法》跟《教育基本法》都規定國家不得為特定宗教宣傳，彩虹媽媽是不是宗教團體，從教材、培訓過程就能夠判定。既然不是每個人都覺得女生是男人的肋骨變來的，我們就讓宗教留在課後的時間就好了。」</p>
        <p>在特定宗教脈絡下，即便已經去宗教化，這些擔任志工的信徒於日常生活用語中容易不小心流漏宗教使用的字眼，無形中，就可能影響孩童對特定宗教的觀感，甚至輕信。</p>
        <p>「唯有教師端、校方端控管這些宗教團體志工，加上家長與社會大眾的警覺，才能維持宗教中立。」陳民峰總結：「否則，校園就會成為各宗各派的競爭舞台，哪個宗教團體『財大人多』就可以獲得行銷機會。」</p>
        <h2>「籠子需要被看見」</h2>
        <p>2019 年 10 月 14 日，在本次爭議爆發以來近兩個月後，周雅淳在行政院國家發展委員會的公共政策網路參與平台，發起了<a href="https://join.gov.tw/idea/detail/085b17a1-caf2-46d1-9673-f8b5e6b6eb3d/endorse?ctoken=1826776929&size=100&page=1#endorses" target="_blank">呼籲教育部制訂中小學校園志工規則的提案</a>。</p>
        <p>在這份提案中，她明列了目前校園志工管理之不足，並建議教育部將規定比照正式教育人員規定——如規範志工資格、訓練、工作範圍、權利義務、罰則等項目。不到一個月後，附議達到 5000 人連署門檻。教育部依規，須於 2 個月內（109 年 1 月5 日前）具體回應。</p>
        <p>對此，教育部國教署回應：「目前積極研擬相關作業，108 年 11 月 20 日邀請地方政府蒐集意見後，研擬《高級中等以下學校邀請校外人士（含志工及民間團體）入校協助教學或活動注意事項》草案，後續將循程序發布。」</p>
        <p>身兼「單親媽媽和她的小孩」專頁創辦人、勵馨基金會花蓮分事務所性別與倡議專員的她寫下：「這個志工規則並非針對特定團體，以我自己而言，提出這個提案，基本上就是『納管我自己』。」</p>
        <p>建立制度，是目前訪問到的家長們和關心此事的議員們共同的解決方案。曾玟學舉例，「地方鄉鎮圖書館的說故事志工，都有 6 至 10 小時的培訓課程，中間會涉及性平和教育相關內容，當你上完這些課，就會取得縣府頒發的說故事志工認證。我希望教育處在學校也建立一個認證的機制，不一定要完全一樣，但有這樣的機制，等於縣府先做了把關，家長也會比較安心。」</p>
        <p>他也提到，志工說故事的教材必須要定期公布在學校或教育處的網站上，「我非常希望教育處能來開說明會，讓正反雙方針對教材內容討論。如果家長對某些繪本故事、或引用聖經的話有所質疑，可以好好討論清楚。如果這些教材家長看完、學校、教育處、甚至我們這些民意代表都覺得 OK，這些爭議都會降低。」</p>
        <p>桃園市政府教育局也在桃園的<a href="https://udn.com/news/story/7324/4112406" target="_blank">彩虹媽媽爭議</a>爆發後，主動要求市內的學校回報校外團體入校教學的情形，並要求學校依照教育部相關準則的規定，入班教學的教材都要經過審查。但如何避免教學現場跟遞交的資料不一樣？桃園市教育局回應，教材既然經過審查，也規定老師要陪同教學，如果有問題，就是老師要負責。</p>
        <p>「取消教師晨會」，也是多名身在教學現場多年教師的心聲。教書資歷超過20 年、新北市蘆洲區鷺江國民小學資深教師翁麗淑這樣形容，「強制級任老師出席晨會，其實也是一種威權的體質。」目前，已經有許多學校將原本放在晨光時間的晨會，移到孩童放學後的夕會時段，且成效良好。透過取消晨會，讓導師能夠在場。也能減少志工脫軌事件的機會。</p>
