        <h2>求助無門的家長</h2>
        <p>家住臺北市的麗雲（化名）兒子今年剛上小學，開學前，待回覆的文件堆裡一張「彩虹媽媽」志工招募宣傳單吸引她的目光。麗雲搜尋了彩虹媽媽的官網，覺得從組織的名字（彩虹）、網站看起來都是很友善的團體，便沒有多想。</p>
        <p>隔了幾天，由於新竹縣興隆國小的<a href="https://tw.appledaily.com/new/realtime/20190907/1629676/" target="_blank">彩虹媽媽事件</a>上了新聞，媒體報導興隆國小二年級的晨光時間，被質疑導入了彩虹愛家生命教育課程，含有宗教和性別刻板印象的內容。麗雲從其他家長那裡聽到一些討論，「有些家長很在乎，覺得宗教就是不能進入校園，不喜歡小孩有這種被洗腦的可能性。」麗雲也同樣氣憤，她並不反對志工，但想知道他們實際教了孩子什麼，卻無從暸解起。</p>
