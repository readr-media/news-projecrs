      <h1>爭點三：一卡多用好可怕，我可以不要使用數位身分證嗎？</h1>
      <h2 class="sub">政府讓步：可以</h2>
      <div class="intro intro--report">
        <p>其實臺灣政府不是第一次推動「一卡多用」的超級卡片，1998 年，就曾有結合身分證、健保卡、電子簽章、指紋等功能的「國民卡」BOT 計畫，但最後因反對一卡多用的聲音而終止。同時具有政府和學界經驗的中研院資訊科學研究所研究員何建明，當時也是反對者。事隔多年，他再次站上反對數位身分證的前線，堅定反對「一卡多用」，他認為，政府不能一味跟民眾強調「多方便」，卻避談可能的風險。</p>
        <p>何建明表示，政府裡有關於報稅、健保、戶籍、財產權等等各式各樣的資料庫，目前都是獨立分開的。這些資料是否能靠一張卡或一組帳號密碼就全部拿到？政府應該給出限制。這一項在 READr 的調查中，意見也相當分歧。</p>
      </div>
      <ReportFigure src="infographic/8" />
      <div class="intro intro--report">
        <p>何建明表示，他很期待能有一個更貼近人民需求的智慧政府，「但不能老是遇到數位身份認證或識別，就想到要做身分證。」他指出，《戶籍法》有規定「國民身分證應隨身攜帶」，身分證是國家強制人民要拿的，但很多政府服務並不是每個人都需要。舉例來說，報稅一年只要用一次自然人憑證，「平常你可以把自然人憑證鎖在保險櫃，報稅時再拿出來，但身分證你沒有選擇。」</p>
        <p>未來的身份驗證，「密碼」會是關鍵。與櫃檯人員面對面確認身份的流程，將由「輸入密碼驗證身份」取代。基於密碼學的公私鑰配對驗證<NotationUnfolded><p>依照內政部規劃，數位身份的驗證是透過公鑰、私鑰的配對來完成。私鑰儲存在個人的晶片卡中，是透過數學公式運算得出的結果。有了私鑰後，再運算生成公鑰，由私鑰運算生成公鑰的過程，理論上是無法逆推破解的。有了公鑰之後，就等於是有了「在網路上裝資訊的信箱」，使用者可以把公鑰發給各個公私部門，讓它們把資料放進信箱，使用者的私鑰可以視為開啟信箱的鑰匙，使用者插卡、輸入密碼同意，就像拿私鑰解鎖信箱一樣。</p></NotationUnfolded>，理論上夠安全，要計算能力非常強大的電腦才有可能破解。</p>
        <p>內政部回應，數位身分證是提供民眾多一種享受網路便利服務的選擇，民眾可自由選擇是否要附加自然人憑證，所以臨櫃服務不會取消。現行自然人憑證卡仍持續發行，讓不希望整合在同一張卡的民眾仍可使用智慧政府服務。</p>
        <p>但何建明從實務應用層面提出懷疑：不諳電腦操作的長者、過去讓代書、旅行社職員代辦服務的人們、不會上網的「數位弱勢」族群，是否就要把自己的密碼交給他人使用？使用者的密碼真的安全嗎？使用者如果設定很簡單的密碼，是不是就很容易被破解、被冒用身份？若有心人士藉由釣魚簡訊、釣魚郵件要騙使用者輸入密碼該怎麼防？</p>
        <p>「2019 臺灣網路報告」顯示，55 歲以上有上網經驗者僅佔七成，相比其他世代皆超過 96% 的上網率，遠遠落後。</p>
        <p>何建明表示，過去政府推動自然人憑證、健保卡，較少被反對，因為這是「專卡專用」。他強調自己並非反對國家走向「智慧政府」，他建議，使用教育類的線上服務用一組帳號密碼，使用財稅類的服務用另一組，透過技術連接有關部門的網站，減少 100 個部門有 100 個帳號密碼這種極端麻煩的情況，但也不要一組帳號密碼打天下。</p>
      </div>

      <h2 class="sub">資料串接後，單點被破解就全部淪陷？</h2>
      <div class="intro intro--report">
        <p>臺灣政府仿造國際的數位政府規格，打造了一條「T-Road」<NotationUnfolded><p>T-Road 是什麼？</p><p>未來民眾可以用數位身分證當作「鑰匙」，打開存在政府各部門的電子化資料。跨部門間的資料傳輸，會透過在政府內部網路（正式名稱是政府骨幹網路 GSN，電子化的公文、人事資料、民眾申辦資料……等全國各級政府機關的業務資料，都在這個網路環境當中）建造的資料交換通道 T-Road 來傳遞，國發會表示，這會比以往臨櫃紙本申辦更快速，且透過 T-Road 的多重加密技術，資料更安全。未來民眾要使用政府服務，可使用數位身分由 T-Road 入口網登入，使用在政府骨幹網路裡的資料。</p><ReportFigure src="infographic/1-1" /><p>若涉及資料交換時，A 機關若要和 B 機關交換資料，A 機關的資料中心透過 T-Road ，在政府骨幹網路中與 B 機關交接資料，這過程中，T-Road 當中的「T-Road 管理平臺」和「資料交換防護機制」可以統一資料規格並做好安全防護，要交換的資料將會從 T-Road 回到 B 機關的資料中心，完成 A、B 機關間的資料交換。</p><ReportFigure src="infographic/1-2" /></NotationUnfolded>，意即資料在政府部門間穿梭的通道，若一個部門的資料庫被攻破，駭客會不會沿著 T-Road 流竄到其他部門？淡江大學資訊管理學系教授黃明達認為，民眾個資存在各部會，有各自獨立的資安系統，不太可能單點突破。</p>
