      <h2 class="sub">誰可以使用我的數位資料？</h2>
      <div class="intro intro--report">
        <p>除了政府以外，未來隨著數位身分證使用普及，民間私部門也會用來做身份認證，不管使用者有沒有開通自然人憑證，卡片本身都已經可以被機器讀取，就會留下使用者的紀錄，透過這些紀錄，是不是就可以勾勒出一個人的生活軌跡？有心人士是否可以藉此追蹤他人？</p>
