      <h2 class="sub">資料存在政府裡，夠安全嗎？</h2>
      <div class="intro intro--report">
        <p>臺灣政府推行數位身分證，最常以數位化政府堪稱世界典範的愛沙尼亞為例。但愛沙尼亞也不全然是數位化政府的烏托邦，2007 年春天，愛沙尼亞遭到疑似源自俄羅斯的大規模駭客攻擊，當時政府、銀行及許多網站停擺，損失慘重。此次事件後，愛沙尼亞研發網路安全戰略，2018 年在盧森堡開設全球首座「數據大使館」，主要用來備份如國民身分證、稅金、年金、不動產、企業登記等國家級數位資料。</p>
        <p>2017 年，位於捷克的密碼暨安全研究中心發現，德國半導體業者英飛凌（Infineon）生產的晶片含有演算法漏洞，加密的身分證可能因此被破解，有 75 萬張使用德國業者晶片的愛沙尼亞身分證暴露在資安風險中，需要停用更新。總部位於美國的高等計算系統協會（USENIX）刊登一篇預計在今年 8 月正式發表的論文，愛沙尼亞資安學者 Arnis Parsovs 發現，愛沙尼亞數位身分證的製造過程存在缺失，造成部分數位身分證有被他人複製的可能，數位身分證的安全性再次受到質疑。</p>
        <p>在臺灣，根據政府單位資安事件通報統計，2018 年共有 262 件資安事件通報，當中嚴重等級排第二位的三級事件有 6 件。在 520 總統就職前夕，總統府遭駭客入侵，內部文件遭變造外流。國內外資安事件頻傳，未來數位身分證連結更多數位服務，累積更多數位個資，政府是否有能力好好保護？</p>
        <p>內政部回應對於「政府資安保護能力」的提問，澄清臺灣目前為止並沒有自然人憑證加密被破解的事件。2011 年起已加強自然人憑證的卡片金鑰強度，目前金鑰尚屬安全。2012 年間曾發現 163 張有被破解風險的「弱金鑰」自然人憑證卡片，這是因為 2006 年生產的卡片有晶片瑕疵及參數錯誤，發現之後，內政部即對所有自然人憑證用戶的金鑰進行全面檢測，2012 年主動更換新憑證給受影響民眾使用後，目前為止沒有加密被破解的事件。</p>
        <p>內政部對晶片及系統的資安，分別訂有規格及防範機制。黃彥男認為，以晶片卡的規格來說，已經是最高等級，資料保護能力應該是足夠的。「如果規格是對的、應用是對的，（數位身分證）照理說會比現在的紙本身分證要安全。」</p>
        <p>黃彥男強調，數位身分證和政府資料庫是兩件不同的事，不能混為一談。沒有數位身分證，駭客一樣能進到政府資料庫裡去偷東西。很多時候政府發生資安事件，不是技術問題，而是使用者人為的管理問題。「要駭客完全攻不到，就是把資料全部刪掉，或資料全部不要連網。」但這又造成效率差、服務差的問題，不太可能再回到這種低度數位化的狀態。</p>
        <p>他指出，臺灣政府對資安的保護程度一直在進步，駭客的功力也在進步，資安沒有完美的一天，只能不斷去預防、修正。</p>
        <p>但正因為很多資安事件事後檢討是人為操作疏失，何建明認為，政府在「規範人的使用行為」這一部分，到目前還不明確。他質疑，為什麼不是前期仔細規劃，而是後期補漏洞？這樣的政府真的可以信任？「要盲目地說『我相信政府的資安能力』，是有很大風險的。」</p>
