      <h1>爭點二：法律完備了嗎？應就資安及隱私風險立法或修法</h1>
      <h2 class="sub">政府：不用立專法，也無修法必要</h2>
      <div class="intro intro--report">
        <p>除了沒有專責機構，民間團體也認為現有的法律不夠完備。如愛沙尼亞、德國都有為數位身分證的發行訂定專法，明確以法條限制數位個資的傳輸、利用，何明諠認為，臺灣最理想的情況是訂定數位身分證專法，並讓人民可以自由選擇是否領取數位身分證。</p>
        <p>他分析，現在臺灣的個資法「容易被寬鬆解釋」，法條中對個資的運用規範很抽象，公務機關在「為維護國家安全或增進公共利益所必要」時，可以在原本收集個資的目的之外使用。</p>
        <p>但究竟「國家安全」、「公共利益」的界線在哪裡？政府什麼時候可以擴張權力使用民眾的個資，沒有明確的法律規範。</p>
        <p>例如在武漢肺炎防疫中，因應「防疫需要」而串連民眾的出入境資料與健保資料；在健保資料上對醫護人員、空服員等「高風險」職業做註記；透過民眾的手機信號得知民眾的行蹤等等，都是民眾事前不知情、過程也無法說不的例子。</p>
        <p>但「立專法」的過程與時間難以估計，民間團體也可以接受從修法開始做起。</p>
        <p>開放文化基金會認為，因為新的數位身分證與傳統紙本身分證，使用方式有很大變革，《戶籍法》應該新增「數位國民身分證與自然人憑證」一章，規範有別於紙本身分證的數位個資使用。</p>
        <p>例如：數位身分證裝有晶片，可能具有追蹤人民行蹤的功能，應該修改原本《戶籍法》對於身分證應隨身攜帶的規定；或是政府應完整公布數位身分證及自然人憑證的驅動程式原始碼，讓社會大眾自由研究、修改，畢竟涉及國民隱私的數位個資，若存取、讀取技術都只掌握在特定廠商的封閉系統當中，一旦遭到有心人士破解，民眾的隱私會大受影響。</p>
        <p>國發會認為，是否換發數位身分證、是否由民眾自由決定開通電子身份驗證功能等議題，回歸《戶籍法》就足夠，不需要訂定專法。至於民間團體建議加強個資法，國發會則以「審酌各國立法例，都沒有看到在《個資法》訂定相關規定的前例」為由認為無修法必要。</p>
        <p>而關於民間團體建議、內政部主管的《戶籍法》修法，內政部則回應，第 52 條授與內政部訂定身分證的「格式」與「內容」的權責，身分證納入「晶片」，就是有關身分證「格式」與所載「內容」的調整，不需要再修。</p>
        <p>內政部也認為，在個人隱私、資訊安全的保護方面，現有的《資通安全管理法》、《電子簽章法》、《個資法》等法規已經建構綿密的保護網，無需再另訂專法；內政部也曾發函詢問各主管機關，是否配合新版數位身分證修法？但「相關機關均表示並無修法之必要」。</p>
