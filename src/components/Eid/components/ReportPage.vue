        <p>T-Road 的主責單位國發會回應，T-Road 是政府資料專屬的傳輸通道，傳輸過程使用特定的內部網路，外界無法介接 T-Road 取得資料。而且目前分別存在各機關的資料庫，存取權限與密碼是各機關依資安規範分別設定的，資安防護工作由各機關負責，具有不同的防護措施，因此資料不會無限制地被串接，也不會發生經由某一機關連結其他機關資料庫的情形。</p>
