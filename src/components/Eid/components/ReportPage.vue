      <h1>爭點四：攜帶數位身分證等於被政府時時監控？</h1>
      <h2 class="sub">政府：要靠很近才感應得到，感應到也不能做什麼事</h2>
      <div class="intro intro--report">
        <p>去年 9 月，當時的國民黨籍立委許毓仁針質疑，數位身分證具有 RFID （無線射頻辨識）功能，等於政府在人民身上安裝追蹤器。對此內政部嚴正否認，表示身分證中的晶片需要近到以「公分」為單位才能感應，不存在遠端感應的功能。且民眾要自行輸入密碼，加密區的資料才能被取用，單單感應到身分證也不能做什麼事。</p>
        <p>內政部戶政司長張琬宜也表示數位身分證不會主動發送訊號、不會洩漏位置、更不會蒐集民眾使用軌跡，晶片一旦寫入個人化資料就會啟動亂數序號機制，根本無法追蹤，民眾可以放心。</p>
        <p>根據最新的招標書規格，數位身分證的非接觸介面採用 ISO-14443，工作頻率 13.56 MHz，在頻帶分類上屬於次低的「高頻」（HF），一般讀取範圍在 10 公分，理論上能讀取的距離以 1.5 公尺為限。高速公路的感應收費系統，使用的多是頻率最高的「微波」（Microwave） ，讀取距離為 3  公尺至 10 公尺。</p>
        <p>但李柏鋒指出，網路上早有文章教學如何透過工具直接在 50 公分內讀取。而根據臺大鄭振牟教授的論文，透過其他技術，有把握能讀到的距離是 2 公尺，理論上應可達到 10 幾公尺。李柏鋒曾對內政部提出「是不是能關掉數位身分證中的 RFID 功能？」，政府只回應可選擇是否開啟自然人憑證，迴避對晶片感應功能的質疑。</p>
