<script>
import { READR_SITE_URL } from 'src/constants'
import { GOOGLE_SHEET_ID_EID, GOOGLE_SHEET_RANGES_EID } from 'api/config.js'
import { getSheetWithoutRedis, appendSheet } from 'src/api/index.js'
import { situations, results } from '../data/situations.js'
import ReportSituation from './ReportSituation.vue'
import ReportFigure from './ReportFigure.vue'
import NotationUnfolded from './NotationUnfolded.vue'
import DonateBlock from './DonateBlock.vue'

const [, RANGE_SITUATION] = GOOGLE_SHEET_RANGES_EID
const SHARED_URL = `${READR_SITE_URL}eid`

export default {
  name: 'ReportPage',
  components: {
    ReportSituation,
    ReportFigure,
    NotationUnfolded,
    DonateBlock
  },
  data () {
    return {
      situations,
      defaultValues: Array.apply(undefined, { length: 6 }),
      hasCheckedSituation: false,
      agrees: [1, 1, 1]
    }
  },
  computed: {
    result () {
      return results[this.agrees.join('')]
    }
  },
  async mounted () {
    const percentages = await this.fetchPercentageOfSituations()
    this.situations.forEach(function fillInPercentages (situation) {
      const [disagree, agree] = percentages.splice(0, 2)
      situation.percentages = { disagree, agree }
    })
  },
  methods: {
    shareToFb () {
      window.open(`https://www.facebook.com/share.php?u=${SHARED_URL}`)
    },
    shareToLine () {
      window.open(`https://line.me/R/msg/text/?${SHARED_URL}`)
    },
    async fetchPercentageOfSituations () {
      const response = await getSheetWithoutRedis({
        params: {
          spreadsheetId: GOOGLE_SHEET_ID_EID,
          range: `${RANGE_SITUATION}!A1:F1`
        }
      })
      return response.body?.[0] ?? []
    },
    handleSubmit (values, replacerIdx) {
      this.hasCheckedSituation = true
      this.agrees.splice(replacerIdx, 1, values[1])

      const submittedValues = [
        ...this.defaultValues.slice(0, replacerIdx * 2),
        ...values,
        ...this.defaultValues.slice(replacerIdx * 2 + 2)
      ]

      appendSheet({
        params: {
          spreadsheetId: GOOGLE_SHEET_ID_EID,
          range: `${RANGE_SITUATION}!A:F`,
          valueInputOption: 'RAW',
          resource: { values: [[...submittedValues, Date.now()]] }
        }
      })
    },
    agreeText (agree) {
      return agree === 1 ? '同意' : '不同意'
    }
  }
}
</script>

